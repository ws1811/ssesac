package basic;

public class OpTest2 {

	public static void main(String[] args) {
		int a = 7, b = 4;
		System.out.println(a > b); //true
		System.out.println(a < b); //false
		System.out.println(a >= b); //true
		System.out.println(a <= b); //false
		System.out.println(a == b); //false
		System.out.println(a != b); //true

		
	}

}
