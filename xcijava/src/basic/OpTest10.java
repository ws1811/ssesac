package basic;

public class OpTest10 {

	public static void main(String[] args) {
		int a = 10;
		a += 2; // a = a + 2;
		System.out.println(a);//12
		a -= 2; // a = a - 2;
		System.out.println(a);//10
		a *= 2; // a = a * 2;
		System.out.println(a);//20
		a /= 2; // a = a / 2;
		System.out.println(a);//10
		a %= 2; // a = a % 2;
		System.out.println(a);//0


	}

}
