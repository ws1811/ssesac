package basic;

public class OpTest1 {

	public static void main(String[] args) {
		int a=7, b=4;
		System.out.println(a + "+" + b + "=" + (a+b));
		System.out.println(a + "-" + b + "=" + (a-b));
		System.out.println(a + "*" + b + "=" + (a*b));
		System.out.println(a + "/" + b + "=" + ((double)a/b));
		System.out.println(a + "%" + b + "=" + (a%b));
	}

}
